package com.english.english_vision.pojo;

public class Score {
    private Integer scoreid;

    private Integer examcode;

    private Integer studentid;

    private Integer level;

    private Integer score;

    private String answerdate;

    private Integer wordsNumers;

    public Integer getScoreid() {
        return scoreid;
    }

    public void setScoreid(Integer scoreid) {
        this.scoreid = scoreid;
    }

    public Integer getExamcode() {
        return examcode;
    }

    public void setExamcode(Integer examcode) {
        this.examcode = examcode;
    }

    public Integer getStudentid() {
        return studentid;
    }

    public void setStudentid(Integer studentid) {
        this.studentid = studentid;
    }

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public Integer getScore() {
        return score;
    }

    public void setScore(Integer score) {
        this.score = score;
    }

    public String getAnswerdate() {
        return answerdate;
    }

    public void setAnswerdate(String answerdate) {
        this.answerdate = answerdate;
    }

    public Integer getWordsNumers() {
        return wordsNumers;
    }

    public void setWordsNumers(Integer wordsNumers) {
        this.wordsNumers = wordsNumers;
    }
}